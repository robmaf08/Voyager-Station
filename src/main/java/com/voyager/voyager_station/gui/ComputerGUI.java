    protected class PasswordAction extends AbstractAction {

        @Override
        public void actionPerformed(ActionEvent e) {
            insertPassword();
        }
    }

    protected class SearchRoomAction extends AbstractAction {

        @Override
        public void actionPerformed(ActionEvent e) {
            searchRoom();
        }
    }

    private void insertPassword() {
        if (Arrays.equals(COMPUTER_PASSWORD.toCharArray(), jPasswordField.getPassword())) {
            txtNameCamera.setEnabled(true);
            btnRoom.setEnabled(true);
            jPasswordField.setEnabled(false);
            btnPassword.setEnabled(false);
        } else {
            jPasswordField.setText("");
            JOptionPane.showMessageDialog(null, "La password inserita non Ã¨ corretta! Riprovare.", "Errore password", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void searchRoom() {
        try {
            String password;
            PasswordRooms.getInstance().connect();
            if (!txtNameCamera.getText().isEmpty()) {
                password = PasswordRooms.getInstance().searchRoomPassword(txtNameCamera.getText().toLowerCase());
                if (password != null) {
                    txtPasswordRoom.setText(password);
                } else {
                    JOptionPane.showMessageDialog(null, "Nessuna camera disponibile", "Camera inesistente", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Nessuna camera inserita!", "Camera non specificata", JOptionPane.ERROR_MESSAGE);
            }
            PasswordRooms.getInstance().disconnect();
        } catch (SQLException ex) {
            Logger.getLogger(ComputerGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

